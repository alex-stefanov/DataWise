namespace DataWise.Common.Constants;

/// <summary>
/// Specifies the different types of charts available for data visualization.
/// </summary>
public enum ChartType
{
    /// <summary>
    /// Represents a line chart, typically used for trends over time.
    /// </summary>
    Line = 0,

    /// <summary>
    /// Represents a bar chart, useful for comparing different categories.
    /// </summary>
    Bar = 1,

    /// <summary>
    /// Represents a pie chart, used for showing proportions of a whole.
    /// </summary>
    Pie = 2
}
